public class GovernmentReform {
    private String name;
    private int customCost;

    public GovernmentReform(String name, int customCost) {
        this.name = name;
        this.customCost = customCost;
    }

    public String getName() {
        return name;
    }

    public int getCustomCost() {
        return customCost;
    }
}
